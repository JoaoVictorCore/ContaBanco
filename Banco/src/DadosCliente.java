public class DadosCliente {
    private String nomeCliente;
    private int contaCliente;
    private String agenciaCliente;
    private double saldoCliente;

    public String getNomeCliente() {
        return nomeCliente;
    }

    public void setNomeCliente(String nomeCliente) {
        this.nomeCliente = nomeCliente;
    }

    public int getContaCliente() {
        return contaCliente;
    }

    public void setContaCliente(int numeroCliente) {
        this.contaCliente = numeroCliente;
    }

    public String getAgenciaCliente() {
        return agenciaCliente;
    }

    public void setAgenciaCliente(String agenciaCliente) {
        this.agenciaCliente = agenciaCliente;
    }

    public double getSaldoCliente() {
        return saldoCliente;
    }

    public void setSaldoCliente(double saldoCliente) {
        this.saldoCliente = saldoCliente;
    }
}
